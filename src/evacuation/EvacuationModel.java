package Evacuation;

public class EvacuationModel {

    private int evacID;
    private int disasterID;
    private int beneID;

    public int getEvacID() {
        return evacID;
    }

    public void setEvacID(int evacID) {
        this.evacID = evacID;
    }

    public int getDisasterID() {
        return disasterID;
    }

    public void setDisasterID(int disasterID) {
        this.disasterID = disasterID;
    }

    public int getBeneID() {
        return beneID;
    }

    public void setBeneID(int beneID) {
        this.beneID = beneID;
    }

}
